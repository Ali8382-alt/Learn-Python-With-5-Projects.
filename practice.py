#Using While Loop Created by Table in python:
n = int(input("Enter your number: "))
i = 1

while i < 11:
    print(n, "x", i, "=", n * i)
    i += 1

#Using for Loop Created by Table in python:
n = int(input("Enter your number: "))
i=1

for i in range(1,11):
    print(n, "x", i, "=", n * i)
    


